    {
        $is_save = OrderRef::Create(['order_ref_no'=>$r->order_ref]);

        if($is_save){
            return redirect()->back()->with(['msg'=>'Data Inserted Successfully','status'=>'success']);
        }
        else{
            return redirect()->back()->with(['msg'=>'Data Couldn\'t insert','status'=>'danger']);
        }

